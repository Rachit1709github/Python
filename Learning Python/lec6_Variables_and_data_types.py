"""
Variables Hold Different Types Of Data
"""

# Example of array, it is not limited to a specific nature, they are dynamic
array = [[1, "ab", 3, "name"], 1, 5]  # Array variable
print("Array:", array)

# tuple example, tuple is the static form of python array
tuple1 = ((1, 2, 3), "parth", "$HI")
# string example
s = "187rock89jhu"
print("\nString:", s)

# int variable
a = -123456789
print("\nInteger:", a)

# float variable
f = 1.1
print("float variable=", f)
# complex number
c = complex(8, 2)
print("Complex number=", c)
# Boolean Value
b = True
# dictionary type
dict1 = {"name": "parth", "meaning": "kutta", "Job": "Being a burden"}
print("\nDict type", dict1)

print("\nBoolean Variable=", b)
print("\nTo get the data type of any variable use \"type(a)\" a being the variable ")
print("Data Type Of a=", type(a))
print("Data Type Of s=", type(s))
print("Data Type Of array=", type(array))
print("Data Type Of f=", type(f))
print("Data Type Of c=", type(c))
print("Data Type Of b=", type(b))
print("Data Type Of tuple1=", type(tuple1))
print("Data Type Of dict1=", type(dict1))
